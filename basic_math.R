# Basic math operations in R


# Addition----
x <- 6
y <- 3

result <- x+y
result
print(result)

# Subtraction----
result <- x-y
result
print(result)

# Multiplication
multiplication <- x*y
print(multiplication)

# Division----
x/y

# Exponentiation----
x^y
check <- 6*6*6
check

# Square Root----
x <- 16
sqrt(16)
sqrt(x)

# Absolute Value----
x <- -10
y <- abs(x)
print(y)

# Modulus (Remainder)----
x <- 17
y <- 5
x/y

remainder <- x%%y
remainder

# Floor and Ceiling----
x <- 3.8
floor(x) # Round down to the nearest integer

ceiling(x)  # Round up to the nearest integer











