# Basic math operations in R
# Addition----

x <- 6
y <- 3
result <- x + y


# Subtraction----

result <- x - y


# Multiplication

result <- x * y


# Division----

result <- x / y
print(result)

# Exponentiation----

result <- x^y



# Square Root----
x <- 16
result <- sqrt(x)


# Absolute Value----
x <- -10
result <- abs(x)


# Modulus (Remainder)----
x <- 17
y <- 5
result <- x %% y  # %% operator for integer division


# Floor and Ceiling----
x <- 3.8
floor_result <- floor(x) 		# Round down to the nearest integer
floor_result
ceiling_result <- ceiling(x) 	# Round up to the nearest integer
ceiling_result

