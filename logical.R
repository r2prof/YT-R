# How to handle logical data in R?----

# Sample values
x  <-  1
y  <-  2 

# Check the relationship b/w vars
z <- x > y
      
# Print the class of logical operator 
class(z)

# Standard logical operations
u = TRUE

v = FALSE

# "&" (and), "|" (or), and "!" (negation).

# "&" (and)
u & v       

# "|" (or)
u | v        

# "!" (negation)
!u          
